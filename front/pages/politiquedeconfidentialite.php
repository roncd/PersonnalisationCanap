                <h2>5. Politique de Cookies</h2>


                <h3>Qu'est-ce qu'un "cookie" ?</h3>


                <p>
                    Les cookies sont de petits fichiers texte qu'un site web enregistre sur votre ordinateur ou votre appareil mobile lorsque vous visitez le site.
                    Ils facilitent votre expérience en ligne en sauvegardant les informations de navigation. Grâce aux cookies, les sites peuvent vous garder connecté, se souvenir de vos préférences de site et vous proposer un contenu personnalisé. Les cookies peuvent également être utilisés pour établir des statistiques sur l'expérience de navigation et pour montrer des publicités ciblées.
                </p>

                <p>En général, les cookies peuvent être classés par :</p>

                <p>
                    <b>Domaine :</b>
                <ul>
                    <li>Les cookies de première partie sont émis par un site web qu'un utilisateur consulte directement.</li>
                    <li>Les cookies tiers ne sont pas créés par le site web consulté, mais par un tiers comme Google Analytics, DoubleClick, Facebook, Twitter, LinkedIn, Youtube, Vimеo, etc.</li>
                </ul>
                </p>

                <p>
                    <b>Objectif :</b>
                <ul>
                    <li>Les cookies strictement nécessaires sont requis pour que le site web fonctionne correctement.</li>
                    <li>Les cookies de préférences permettent à un site web de se souvenir des choix que vous avez faits dans le passé.</li>
                    <li>Les cookies de statistiques aident le propriétaire du site web à collecter des données statistiques et à comprendre comment les visiteurs interagissent avec le site web.</li>
                    <li>Les cookies marketing suivent l'activité en ligne de l'utilisateur pour aider les annonceurs à diffuser des publicités plus pertinentes.</li>
                </ul>
                </p>

                <p>
                    <b>Durée :</b>
                <ul>
                    <li>Les cookies de session qui sont effacés lorsque l'utilisateur ferme le navigateur.</li>
